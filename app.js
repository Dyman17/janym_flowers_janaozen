const state = {
  locale: 'ru',
  theme: 'dark',
};

const metaDescription = document.querySelector('meta[name="description"]');
const orderLinks = document.querySelectorAll('[data-order-link]');
const langButtons = document.querySelectorAll('[data-lang]');
const themeToggle = document.querySelector('[data-theme-toggle]');
const themeIcon = document.querySelector('.theme-toggle__icon');
const themeLabel = document.querySelector('.theme-toggle__label');
const langSwitch = document.querySelector('.lang-switch');

function getLocaleCopy(locale) {
  return translations[locale] || translations.ru;
}

function readStorage(key) {
  try {
    return localStorage.getItem(key);
  } catch {
    return null;
  }
}

function writeStorage(key, value) {
  try {
    localStorage.setItem(key, value);
  } catch {
    // Ignore storage failures in restricted browser contexts.
  }
}

function setOrderLinks(message) {
  const href =
    'https://api.whatsapp.com/send/?phone=7752812900&text=' +
    encodeURIComponent(message) +
    '&type=phone_number&app_absent=0';

  orderLinks.forEach((link) => {
    link.href = href;
  });
}

function setLanguageButtons(locale) {
  langButtons.forEach((button) => {
    const active = button.dataset.lang === locale;
    button.classList.toggle('is-active', active);
    button.setAttribute('aria-pressed', String(active));
  });
}

function applyLocale(locale) {
  const copy = getLocaleCopy(locale);
  state.locale = locale;

  document.documentElement.lang = locale;
  document.title = copy.title;
  if (metaDescription) {
    metaDescription.content = copy.description;
  }

  const brandSmall = document.querySelector('.brand__text small');
  const metaSpans = document.querySelectorAll('.topbar__meta span');
  const topbarOrderLabel = document.querySelector('.topbar__actions [data-order-link] .button__label');
  const eyebrow = document.querySelector('.eyebrow');
  const heroTitle = document.querySelector('.hero h1');
  const heroLead = document.querySelector('.lead');
  const heroOrderLabel = document.querySelector('.hero__actions .button--primary .button__label');
  const heroAddressButton = document.querySelector('.hero__actions .button--secondary');
  const heroChips = document.querySelectorAll('.hero__chips span');
  const infoCards = document.querySelectorAll('.info-card');
  const pricingTag = document.querySelector('.price-panel .section-tag');
  const addressTag = document.querySelector('.map-panel .section-tag');
  const pricingTitle = document.querySelector('.price-panel h2');
  const pricingNote = document.querySelector('.panel-note');
  const mapCtaLabel = document.querySelector('.map-panel__cta .button__label');
  const mapAddress = document.querySelector('.map-panel__footer strong');
  const mapCity = document.querySelector('.map-panel__footer span');
  const contactTag = document.querySelector('.contact-panel .section-tag');
  const contactTitle = document.querySelector('.contact-panel h2');
  const contactText = document.querySelector('.contact-panel__text');
  const contactMeta = document.querySelector('.contact-panel__note');
  const contactWhatsAppLabel = document.querySelector('.contact-panel__button--primary .button__label');
  const contactInstagramLabel = document.querySelector('.contact-panel .button--instagram .button__label');
  const footerText = document.querySelectorAll('.footer p');

  if (brandSmall) brandSmall.textContent = copy.brandSmall;
  if (metaSpans[0]) metaSpans[0].textContent = copy.city;
  if (metaSpans[1]) metaSpans[1].textContent = copy.hours;
  if (topbarOrderLabel) topbarOrderLabel.textContent = copy.order;
  if (eyebrow) eyebrow.textContent = copy.eyebrow;
  if (heroTitle) heroTitle.textContent = copy.heroTitle;
  if (heroLead) heroLead.textContent = copy.lead;
  if (heroOrderLabel) heroOrderLabel.textContent = copy.heroOrder;
  if (heroAddressButton) heroAddressButton.textContent = copy.heroAddress;

  heroChips.forEach((chip, index) => {
    if (copy.chips[index]) {
      chip.textContent = copy.chips[index];
    }
  });

  infoCards.forEach((card, index) => {
    const heading = card.querySelector('h2');
    const paragraph = card.querySelector('p');
    const cardCopy = copy.infoCards[index];
    if (heading && cardCopy) heading.textContent = cardCopy.title;
    if (paragraph && cardCopy) paragraph.textContent = cardCopy.text;
  });

  if (pricingTag) pricingTag.textContent = copy.pricingTag;
  if (addressTag) addressTag.textContent = copy.addressTag;
  if (pricingTitle) pricingTitle.textContent = copy.pricingTitle;
  if (pricingNote) pricingNote.textContent = copy.pricingNote;
  if (mapCtaLabel) mapCtaLabel.textContent = copy.mapCta;
  if (mapAddress) mapAddress.textContent = copy.address;
  if (mapCity) mapCity.textContent = copy.cityCountry;
  if (contactTag) contactTag.textContent = copy.contactTag;
  if (contactTitle) contactTitle.textContent = copy.contactTitle;
  if (contactText) contactText.textContent = copy.contactText;
  if (contactMeta) contactMeta.textContent = copy.contactMeta;
  if (contactWhatsAppLabel) contactWhatsAppLabel.textContent = copy.contactWhatsApp;
  if (contactInstagramLabel) contactInstagramLabel.textContent = copy.contactInstagram;
  if (footerText[0]) footerText[0].textContent = copy.footer1;
  if (footerText[1]) footerText[1].textContent = copy.footer2;

  setOrderLinks(copy.orderMessage);
  setLanguageButtons(locale);
  applyThemeLabel();
}

function applyTheme(theme) {
  state.theme = theme;
  document.documentElement.dataset.theme = theme;
  writeStorage('janym-theme', theme);
  applyThemeLabel();
}

function applyThemeLabel() {
  const copy = getLocaleCopy(state.locale);
  const isDark = state.theme === 'dark';

  if (themeToggle) {
    themeToggle.setAttribute('aria-pressed', String(isDark));
    themeToggle.setAttribute(
      'aria-label',
      isDark ? copy.switchToLight : copy.switchToDark,
    );
  }

  if (themeIcon) {
    themeIcon.textContent = isDark ? '☾' : '☼';
  }

  if (themeLabel) {
    themeLabel.textContent = isDark ? copy.themeDarkLabel : copy.themeLightLabel;
  }

  if (langSwitch) {
    langSwitch.setAttribute('aria-label', copy.langSelectorLabel);
  }
}

function initLocale() {
  const storedLocale = readStorage('janym-locale');
  const fallbackLocale = translations[storedLocale] ? storedLocale : 'ru';
  applyLocale(fallbackLocale);
  writeStorage('janym-locale', fallbackLocale);
}

function initTheme() {
  const storedTheme = readStorage('janym-theme');
  const prefersLight =
    typeof window.matchMedia === 'function' &&
    window.matchMedia('(prefers-color-scheme: light)').matches;
  const fallbackTheme = storedTheme || (prefersLight ? 'light' : 'dark');
  applyTheme(fallbackTheme === 'light' ? 'light' : 'dark');
}

langButtons.forEach((button) => {
  button.addEventListener('click', () => {
    const locale = button.dataset.lang || 'ru';
    applyLocale(locale);
    writeStorage('janym-locale', locale);
  });
});

if (themeToggle) {
  themeToggle.addEventListener('click', () => {
    applyTheme(state.theme === 'dark' ? 'light' : 'dark');
  });
}

initTheme();
initLocale();
